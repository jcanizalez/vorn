// ---------------------------------------------------------------------------
// Font-face injection (runs once on import)
// ---------------------------------------------------------------------------
const FONT_ID = 'seti-icons'
let fontInjected = false

function ensureFont(): void {
  if (fontInjected || typeof document === 'undefined') return
  fontInjected = true
  const style = document.createElement('style')
  style.textContent = `
    @font-face {
      font-family: "${FONT_ID}";
      src: url("${setiWoff}") format("woff");
      font-weight: normal;
      font-style: normal;
      font-display: block;
    }
  `
  document.head.appendChild(style)
}

