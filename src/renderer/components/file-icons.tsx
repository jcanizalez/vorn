// ---------------------------------------------------------------------------
interface IconProps {
  size?: number
}

/** File icon rendered as a Seti font glyph */
export function FileTypeIcon({ name, size = 16 }: IconProps & { name: string }) {
  ensureFont()
  const icon = resolveIcon(name)
  return (
    <span
      className="shrink-0 leading-none"
      style={{
        fontFamily: `"${FONT_ID}"`,
        fontSize: size,
        color: icon.color,
        width: size,
        height: size,
        display: 'inline-flex',
        alignItems: 'center',
        justifyContent: 'center',
        WebkitFontSmoothing: 'antialiased',
        MozOsxFontSmoothing: 'grayscale'
      }}
    >
      {icon.ch}
    </span>
  )
}
