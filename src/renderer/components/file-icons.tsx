// ---------------------------------------------------------------------------
// Resolve filename → icon definition
// ---------------------------------------------------------------------------
function resolveIcon(name: string): IconDef {
  const lower = name.toLowerCase()

  // 1. Exact filename match
  if (NAMES[lower]) return D[NAMES[lower]] ?? D._default

  // 2. Compound extension match (e.g. "spec.ts", "css.map")
  const dotIdx = lower.indexOf('.')
  if (dotIdx !== -1) {
    const afterFirst = lower.slice(dotIdx + 1) // e.g. "spec.ts" from "foo.spec.ts"
    if (EXT[afterFirst]) return D[EXT[afterFirst]] ?? D._default

    // Try last two segments: "test.js" from "my.thing.test.js"
    const lastDot = lower.lastIndexOf('.')
    if (lastDot !== dotIdx) {
      const prevDot = lower.lastIndexOf('.', lastDot - 1)
      if (prevDot !== -1) {
        const compound = lower.slice(prevDot + 1)
        if (EXT[compound]) return D[EXT[compound]] ?? D._default
      }
    }
  }

  // 3. Simple extension
  const ext = lower.includes('.') ? lower.split('.').pop()! : undefined
  if (ext) {
    if (EXT[ext]) return D[EXT[ext]] ?? D._default
    if (LANG_EXT[ext]) return D[LANG_EXT[ext]] ?? D._default
  }

  // 4. Dotfiles without extension
  if (lower.startsWith('.')) return D._config

  return D._default
}

